    private void abrirArchivo(int opcion) {
        JFileChooser file = new JFileChooser();
        file.showOpenDialog(this);
        File abre = file.getSelectedFile();
        switch (opcion) {
            case 0:
                rutaLlavePrivada = abre != null ? abre.getAbsolutePath() : null;
                break;
            case 1:
                rutaLlavePublica = abre != null ? abre.getAbsolutePath() : null;
                break;
            case 2:
                rutaArchivo = abre != null ? abre.getAbsolutePath() : null;
                break;
        }
    }

