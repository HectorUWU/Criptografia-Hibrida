    private void btnCifrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCifrarActionPerformed
        // TODO add your handling code here:
        CryptoHibrida ch = new CryptoHibrida();
        Archivo archivo = new Archivo(rutaArchivo);
        try {
            byte[] out = ch.cifrar(archivo.getInfo(), llavePublica);
            archivo.saveBytes("Cifrado.txt", out);
            JOptionPane.showMessageDialog(null, "Archivo cifrado exitosamente");
        } catch (Exception ex) {
            Logger.getLogger(Ventana.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Ocurrió un error");
        }
    }//GEN-LAST:event_btnCifrarActionPerformed

    private void btnCifrarPublicaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCifrarPublicaActionPerformed
        abrirArchivo((byte) 1);
        if (rutaLlavePublica != null) {
            Archivo archivo = new Archivo(rutaLlavePublica);
            lblCifrarRutaPublica.setText(rutaLlavePublica);
            llavePublica = (PublicKey) archivo.leerLlave();
        } else {
            JOptionPane.showMessageDialog(null, "Archivo no válido");
        }
    }//GEN-LAST:event_btnCifrarPublicaActionPerformed

